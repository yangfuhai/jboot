package io.jboot.objects.lock.impl;

import io.jboot.objects.lock.JbootLock;

import java.util.HashMap;
import java.util.Map;
import java.util.concurrent.TimeUnit;
import java.util.concurrent.locks.Condition;
import java.util.concurrent.locks.ReentrantLock;

/**
 * @author michael yang (fuhai999@gmail.com)
 * @Date: 2020/3/7
 */
public class JbootLocalLock implements JbootLock {

    private static Map<String, ReentrantLock> LOCKS = new HashMap<>();

    private ReentrantLock lock;

    public JbootLocalLock(String name) {
        lock = LOCKS.get(name);
        if (lock == null) {
            ReentrantLock newLock = new ReentrantLock();
            LOCKS.putIfAbsent(name, newLock);
        }
    }

    @Override
    public void lock() {
        lock.lock();
    }

    @Override
    public void lockInterruptibly() throws InterruptedException {
        lock.lockInterruptibly();
    }

    @Override
    public boolean tryLock() {
        return lock.tryLock();
    }

    @Override
    public boolean tryLock(long time, TimeUnit unit) throws InterruptedException {
        return lock.tryLock(time, unit);
    }

    @Override
    public void unlock() {
        lock.unlock();
    }

    @Override
    public Condition newCondition() {
        return lock.newCondition();
    }
}
